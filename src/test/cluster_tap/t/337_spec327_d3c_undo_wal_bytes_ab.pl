#
# 337_spec327_d3c_undo_wal_bytes_ab.pl
#	  spec-3.27 D3c — undo WAL-bytes A/B: bufmgr must not amplify vs legacy.
#
#	  D3b's first-cut bufmgr write path emitted an always-FPI (full 8KB page)
#	  RM_CLUSTER_UNDO record PER undo record, which measured ~15x the WAL of the
#	  legacy_pool path (deferred-merge + FPI-on-first-touch delta).  That WAL
#	  amplification -- not the buffer machinery -- was the dominant cause of the
#	  A+B+C write-tax regression (t/336).  D3c makes the bufmgr path use the SAME
#	  FPI-on-first-touch-since-checkpoint + 3-range delta representation as legacy
#	  (Q5-C keeps recovery path-based).
#
#	  This test runs an IDENTICAL fixed workload against a legacy_pool and a
#	  bufmgr instance and compares the WAL bytes each generates.  It isolates the
#	  undo WAL representation from the noisy TPS.  Gate: bufmgr WAL must be within
#	  a small multiple of legacy WAL (<= 3x) -- i.e. no gross per-record FPI
#	  amplification.  (Exact parity is a deferred-merge follow-up; this gate only
#	  proves the 15x defect is gone.)
